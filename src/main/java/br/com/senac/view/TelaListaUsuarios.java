    private void carregarDados() {
        List<UsuarioDTO> usuarios = controller.listarUsuarios();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        usuarios.forEach(u -> model.addRow(new Object[]{
            u.getId(),
            u.getUsername(),
            u.getTipoUsuario()
        }));
    }

