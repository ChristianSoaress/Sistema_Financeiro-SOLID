    private void btnRemoverUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverUserActionPerformed
        int linha = jTable1.getSelectedRow();
        if(linha == -1){
            JOptionPane.showMessageDialog(
                    this, 
                    "Selecione um usuário para remover.", 
                    "Atenção", 
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Object [] opcao = {"Cancelar", "Confirmar"};
        int confirmacao = JOptionPane.showOptionDialog(
                this, 
                "Esta ação não pode ser desfeita. Confirmar exclusão do usuário?", 
                "Confirmar exclusão", 
                JOptionPane.DEFAULT_OPTION, 
                JOptionPane.WARNING_MESSAGE, 
                null, 
                opcao, 
                opcao[0]);
        
        if(confirmacao == 1){
            Integer id = (Integer) jTable1.getValueAt(linha, 0);
        
            try{
                controller.excluirUsuario(id, usuarioAutenticado.getId());
                carregarDados();
                JOptionPane.showMessageDialog(
                        this, 
                        "Usuário removido com sucesso.", 
                        "Sucesso", 
                        JOptionPane.INFORMATION_MESSAGE);
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(
                        this, 
                        "Ocorreu um erro: " + e.getMessage(), 
                        "Erro", 
                        JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnRemoverUserActionPerformed

